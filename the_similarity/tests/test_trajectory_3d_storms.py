"""End-to-end self-similarity backtest on real NOAA HURDAT2 hurricane tracks.

This is the second experiment in the "abstracting the spark" research arc.
Experiment 1 (PR #301) showed that DTW on Frenet (kappa, tau) descriptors
beats persistence, linear extrapolation, and random-analogue baselines on
**synthetic** 3D agent trajectories. This test asks the next obvious
question: *does the same primitive — same descriptors, same matcher,
same forecast cone — beat those baselines on real-world 3D paths?*

We use NOAA's HURDAT2 Atlantic-basin best-track database (1851-2023,
public domain) as the data source. Each storm becomes a (N, 3) trajectory
in (x_km, y_km, z_scaled_wind) coordinates via per-storm equirectangular
projection. The corpus is split chronologically — train on storms with
year <= 2010, test on year > 2010 — to prevent any data leakage.

Predictors compared at horizon J=4 (24 hours):
    persistence:      stay-at-last-point baseline
    linear:           per-axis least-squares extrapolation of the
                      window
    random_analogue:  unweighted average of randomly-picked corpus
                      windows' continuations (isolates "is shape DTW
                      doing anything?")
    model:            Frenet (kappa, tau) -> DTW -> weighted forecast
                      cone (the system under test)

Metrics:
    spatial_mae_km:   mean Euclidean error in km between P50 and actual
                      track position. We use the horizontal (x, y)
                      components only — z is the wind-scale axis whose
                      units are not km, so adding it would corrupt the
                      "user-facing distance" interpretation.
    hit_rate:         did the actual terminal position lie inside the
                      P10/P90 axis-aligned bounding box (in x AND y)?
    crps:             approximated CRPS averaged over (x, y) per bar.

Lifecycle: this test requires the real HURDAT2 parquet at
``the-similarity-data/datasets/storm_tracks/atlantic.parquet``. The
parquet is regenerated by
``the-similarity-data/scripts/fetch_hurdat2.py``. CI gates that don't
have the parquet skip the test cleanly via pytest's
``importorskip`` / ``skipif`` mechanism — there is no in-tree
fallback because the *point* of the experiment is the real data.

Hyperparameters:
    K = 4   -- 24h of 6-hourly fixes (HURDAT2 native cadence)
    J = 4   -- 24h forecast horizon
    top_n = 10   -- analogues weighted into the cone

Pass criteria (relaxed from synthetic's K=50 / J=20):
    - Test harness produces non-zero trials for all four predictors.
    - The script PRINTS the result table; a non-passing model is
      not considered a test failure here. The point is the result,
      not green CI. (The PR body / concept note carry the actual
      verdict.)

The test is marked ``@pytest.mark.slow`` and additionally skipped
when the parquet is missing.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from pathlib import Path
from typing import List

import numpy as np
import pytest

from the_similarity.core.trajectory_matcher import build_corpus, forecast_cone
from the_similarity.datasets.storm_tracks import Storm, load_storms


_REPO_ROOT = Path(__file__).resolve().parents[2]
_PARQUET_PATH = (
    _REPO_ROOT / "the-similarity-data" / "datasets" / "storm_tracks" / "atlantic.parquet"
)


# ---------------------------------------------------------------------------
# Pure-numpy predictors
# ---------------------------------------------------------------------------


@dataclass
class BacktestResult:
    """Aggregate metrics from one predictor's walk-forward backtest."""

    name: str
    n_trials: int
    spatial_mae_km: float = 0.0
    hit_rate: float = 0.0
    crps: float = 0.0
    per_trial_mae: List[float] = field(default_factory=list)


def _persistence_forecast(query_points: np.ndarray, forward_bars: int) -> dict:
    """Baseline: storm stays at its last position.

    Persistence is the textbook null hypothesis for short-horizon
    track forecasting. We add a small uncertainty band so the
    hit_rate metric is well-defined; without it the cone has zero
    width and the actual track can never enter it.
    """
    last = query_points[-1]
    p50 = np.tile(last[None, :], (forward_bars, 1))
    # Epsilon-cone of 50 km so persistence isn't rewarded with a
    # trivially-impossible hit rate. Width chosen to match typical
    # 6-hour storm displacement (~30-50 km).
    eps = np.array([50.0, 50.0, 50.0])  # km, km, wind*5 km
    return {"P10": p50 - eps, "P50": p50.copy(), "P90": p50 + eps}


def _linear_forecast(query_points: np.ndarray, forward_bars: int) -> dict:
    """Baseline: per-axis least-squares extrapolation of the window.

    Equivalent to an ARMA(1, 0) / pure-momentum forecast. The
    uncertainty band scales the per-axis residual std with sqrt(h).
    """
    K = len(query_points)
    t = np.arange(K)
    coeffs = []
    for axis in range(3):
        slope, intercept = np.polyfit(t, query_points[:, axis], deg=1)
        coeffs.append((slope, intercept))
    future_t = np.arange(K, K + forward_bars)
    p50 = np.column_stack([c[0] * future_t + c[1] for c in coeffs])
    residuals = []
    for axis in range(3):
        slope, intercept = coeffs[axis]
        pred = slope * t + intercept
        residuals.append(np.std(query_points[:, axis] - pred))
    band = np.array(residuals) * np.sqrt(np.arange(1, forward_bars + 1))[:, None]
    return {"P10": p50 - 1.28 * band, "P50": p50.copy(), "P90": p50 + 1.28 * band}


def _random_analogue_forecast(
    query_points: np.ndarray,
    corpus,
    forward_bars: int,
    rng: np.random.Generator,
    n_picks: int = 10,
):
    """Baseline: unweighted average of n_picks random corpus windows.

    Isolates the question: *is the Frenet-DTW retrieval doing
    anything?* If the model beats persistence but not random-analogue,
    the win comes from the corpus's marginal distribution of
    continuations, not from shape similarity.
    """
    if not corpus.windows:
        return _persistence_forecast(query_points, forward_bars)
    anchor = query_points[-1]
    indices = rng.integers(0, len(corpus.windows), size=n_picks)
    futures = []
    for idx in indices:
        w = corpus.windows[idx]
        if w.future_points is None:
            continue
        delta = w.future_points - w.points[-1]
        if delta.shape[0] >= forward_bars:
            delta = delta[:forward_bars]
        else:
            pad = np.tile(delta[-1:], (forward_bars - delta.shape[0], 1))
            delta = np.concatenate([delta, pad], axis=0)
        futures.append(delta)
    if not futures:
        return _persistence_forecast(query_points, forward_bars)
    futures_arr = np.stack(futures, axis=0)
    p50 = np.median(futures_arr, axis=0) + anchor
    p10 = np.quantile(futures_arr, 0.1, axis=0) + anchor
    p90 = np.quantile(futures_arr, 0.9, axis=0) + anchor
    return {"P10": p10, "P50": p50, "P90": p90}


def _model_forecast(query_points: np.ndarray, corpus, forward_bars: int):
    """The system under test: Frenet-DTW analogue forecast."""
    f = forecast_cone(
        query_points, corpus, forward_bars=forward_bars, top_n=10,
        percentiles=(10, 50, 90),
    )
    if f.n_analogues == 0:
        return _persistence_forecast(query_points, forward_bars)
    return {"P10": f.curves[10], "P50": f.curves[50], "P90": f.curves[90]}


# ---------------------------------------------------------------------------
# Spatial metric helpers
# ---------------------------------------------------------------------------


def _evaluate_forecast_xy(forecast: dict, actual: np.ndarray) -> tuple[float, bool, float]:
    """Compute (spatial MAE in km, hit_rate, CRPS) using only the (x, y) axes.

    The z axis is wind-scaled (its units aren't km) so we project it
    out for the user-facing metrics. The model is still trained on
    all three axes (that's the point of the 3D embedding); only the
    REPORTING is 2D-projected.
    """
    p50 = forecast["P50"]
    p10 = forecast["P10"]
    p90 = forecast["P90"]
    err = np.linalg.norm(p50[:, :2] - actual[:, :2], axis=1)
    spatial_mae = float(np.mean(err))
    # Hit: terminal actual inside the (x, y) bounding box at P10/P90.
    # We deliberately only check the horizontal axes — z hit-rate is
    # confounded by the wind-scale ablation we run elsewhere.
    hit = bool(
        np.all(actual[-1, :2] >= p10[-1, :2])
        and np.all(actual[-1, :2] <= p90[-1, :2])
    )
    crps_vals = []
    cdf_levels = np.array([0.1, 0.5, 0.9])
    # Per-axis, per-bar CRPS approximation across (x, y) only.
    for axis in range(2):
        for bar in range(p50.shape[0]):
            forecast_terminals = np.array(
                [p10[bar, axis], p50[bar, axis], p90[bar, axis]]
            )
            indicators = (actual[bar, axis] <= forecast_terminals).astype(float)
            crps_vals.append(float(np.mean((indicators - cdf_levels) ** 2)))
    crps = float(np.mean(crps_vals))
    return spatial_mae, hit, crps


def _walk_forward_backtest(
    test_storms: List[Storm],
    forecaster,
    K: int = 4,
    J: int = 4,
    stride: int = 4,
    name: str = "model",
) -> BacktestResult:
    """Walk-forward over every test storm; aggregate per-predictor metrics."""
    spatial_errors: List[float] = []
    hits: List[bool] = []
    crpses: List[float] = []
    for s in test_storms:
        T = s.points.shape[0]
        if T < K + J:
            continue
        for start in range(0, T - K - J + 1, stride):
            query = s.points[start : start + K]
            actual = s.points[start + K : start + K + J]
            try:
                forecast = forecaster(query)
            except Exception:
                continue
            mae, hit, crps_val = _evaluate_forecast_xy(forecast, actual)
            spatial_errors.append(mae)
            hits.append(hit)
            crpses.append(crps_val)
    if not spatial_errors:
        return BacktestResult(name=name, n_trials=0)
    return BacktestResult(
        name=name,
        n_trials=len(spatial_errors),
        spatial_mae_km=float(np.mean(spatial_errors)),
        hit_rate=float(np.mean(hits)),
        crps=float(np.mean(crpses)),
        per_trial_mae=spatial_errors,
    )


# ---------------------------------------------------------------------------
# Helpers shared with the ablation test
# ---------------------------------------------------------------------------


def _split_train_test(z_scale: float = 5.0):
    """Load + chronologically split HURDAT2.

    train: year <= 2010    (pre-2011 storms)
    test:  year >  2010    (2011-2023 storms)

    The split is hard-coded by year so the function is deterministic
    given the upstream parquet. Subsampled test set (100 storms) keeps
    runtime manageable for the slow-marked test; subsample is seeded
    for reproducibility.
    """
    storms = load_storms(_PARQUET_PATH, min_fixes=8, z_scale=z_scale)
    train = [s for s in storms if s.year <= 2010]
    test = [s for s in storms if s.year > 2010]
    rng = np.random.default_rng(42)
    if len(test) > 100:
        idx = rng.choice(len(test), size=100, replace=False)
        test = [test[int(i)] for i in idx]
    return train, test


def _run_predictor_table(z_scale: float, K: int = 4, J: int = 4):
    """Run all four predictors at one z-scale; return list of BacktestResults.

    The corpus is built once per z-scale (since z_scale changes the
    points). All four predictors run against the same corpus +
    test set so the n_trials column is identical across rows.
    """
    train, test = _split_train_test(z_scale=z_scale)
    trajectories = [s.points for s in train]

    corpus = build_corpus(
        trajectories,
        window_len=K,
        stride=1,
        forward_bars=J,
        # sigma_pos=1.0 is gentler than the default 2.0; storms have
        # only 4-point windows so heavy pre-smoothing flattens the
        # signal completely. See concept note for the experiment that
        # picked this value.
        sigma=1.5,
        sigma_pos=1.0,
    )

    rng = np.random.default_rng(7)

    def model_fc(query):
        return _model_forecast(query, corpus, J)

    def persistence_fc(query):
        return _persistence_forecast(query, J)

    def linear_fc(query):
        return _linear_forecast(query, J)

    def random_fc(query):
        return _random_analogue_forecast(query, corpus, J, rng, n_picks=10)

    bt_persistence = _walk_forward_backtest(
        test, persistence_fc, K=K, J=J, name="persistence"
    )
    bt_linear = _walk_forward_backtest(test, linear_fc, K=K, J=J, name="linear")
    bt_random = _walk_forward_backtest(
        test, random_fc, K=K, J=J, name="random_analogue"
    )
    bt_model = _walk_forward_backtest(test, model_fc, K=K, J=J, name="model")
    return bt_persistence, bt_linear, bt_random, bt_model, len(corpus.windows)


# ---------------------------------------------------------------------------
# The actual experiment
# ---------------------------------------------------------------------------


@pytest.mark.slow
@pytest.mark.skipif(
    not _PARQUET_PATH.exists(),
    reason=(
        "HURDAT2 parquet missing; run "
        "`python the-similarity-data/scripts/fetch_hurdat2.py` first"
    ),
)
def test_storm_tracks_backtest_vs_baselines():
    """Headline experiment: model vs persistence/linear/random on real storms.

    Marked ``slow`` because it runs four full walk-forward backtests
    on a ~35k-window corpus. Should still complete in well under
    two minutes on commodity hardware.
    """
    K = 4
    J = 4
    Z_SCALE = 5.0  # Default — 100 kt -> 500 km, comparable to track extents.

    bt_p, bt_l, bt_r, bt_m, n_windows = _run_predictor_table(
        z_scale=Z_SCALE, K=K, J=J
    )

    # Print a clean summary so the result is visible in CI output
    # whether or not the model "wins". This is the headline number
    # the PR body and concept note cite.
    print()
    print("=== HURDAT2 Self-Similarity Backtest (z_scale=5.0) ===")
    print(
        f"  Corpus: {n_windows} windows from train (year <= 2010)\n"
        f"  Window K={K}, horizon J={J}\n"
        f"  Trials per predictor: {bt_m.n_trials}\n"
    )
    header = f"  {'predictor':<18} {'MAE_km':>10} {'hit_rate':>10} {'CRPS':>10}"
    print(header)
    print("  " + "-" * (len(header) - 2))
    for r in [bt_p, bt_l, bt_r, bt_m]:
        print(
            f"  {r.name:<18} {r.spatial_mae_km:>10.2f} {r.hit_rate:>10.4f} {r.crps:>10.4f}"
        )
    print()

    # Validate harness well-formedness (these MUST hold).
    assert bt_m.n_trials > 0, "Backtest produced zero model trials"
    assert bt_p.n_trials == bt_m.n_trials
    assert bt_l.n_trials == bt_m.n_trials
    assert bt_r.n_trials == bt_m.n_trials

    # Honest verdict — printed but not gating the test. The PR body
    # carries the actual interpretation.
    beats_persistence = bt_m.spatial_mae_km < bt_p.spatial_mae_km
    beats_random = bt_m.spatial_mae_km < bt_r.spatial_mae_km
    print(
        f"  Verdict (informational, not gating):\n"
        f"    model_MAE < persistence_MAE: {beats_persistence}\n"
        f"    model_MAE < random_MAE:      {beats_random}\n"
        f"    model_hit_rate:              {bt_m.hit_rate:.3f}\n"
    )
    # The test asserts the harness ran cleanly — not the experiment
    # outcome. The honest verdict is in the printed table and the
    # concept note.
    assert bt_m.n_trials >= 50, (
        f"Too few trials to draw conclusions: {bt_m.n_trials}"
    )
