"""Tests for the Spatium synthetic data generator.

What we verify here
-------------------
* **Determinism** — same density always produces the same point list.
* **Structural invariants** — point/dataset counts, id uniqueness,
  feature vector field ranges (hurst ∈ [0, 1], etc.).
* **Golden fixture parity** — the output of ``build_points(0)`` must
  match the checked-in JSON fixture byte-for-byte. If the algorithm
  ever drifts this test catches it in CI.
* **JS parity helpers** — ``mulberry32`` seeded with known seeds
  produces the exact values the JS ``datasets.ts`` mulberry32 produces
  (verified out-of-band by running both with seed=123 and comparing
  the first 8 samples).
* **Geometry invariants** — custom datasets on unknown domains still
  get a deterministic ring slot (two runs agree).

The fixture itself was generated by running ``python -m
the_similarity.core.spatium_datasets --density 0 --out …``. If the
algorithm ever changes intentionally, regenerate by re-running the
same command and commit the new JSON alongside the code change.
"""

from __future__ import annotations

import json
import math
from pathlib import Path

import pytest

from the_similarity.core.spatium_datasets import (
    DATASETS,
    DOMAIN_ORDER,
    SERIES_KINDS,
    Dataset,
    build_points,
    distance,
    features,
    gen_series,
    hash_str,
    mulberry32,
    regime_label,
    similarity_from_dist,
    to_export_dict,
)

FIXTURE_PATH = Path(__file__).parent / "fixtures" / "spatium" / "points_d0.json"


# ── PRNG / hashing ──────────────────────────────────────────────────


def test_mulberry32_seed_123_first_samples():
    """Known-good values for mulberry32(123).

    These are the first 4 samples produced by the JavaScript
    implementation at ``datasets.ts::mulberry32`` (captured by running
    the same mulberry32 in Node.js with ``seed=123``). Any drift here
    breaks cross-language parity for every downstream output.
    """
    rng = mulberry32(123)
    samples = [rng() for _ in range(4)]
    assert samples[0] == pytest.approx(0.7872516233474016, rel=0, abs=1e-15)
    assert samples[1] == pytest.approx(0.1785435655619949, rel=0, abs=1e-15)
    assert samples[2] == pytest.approx(0.49531551403924823, rel=0, abs=1e-15)
    assert samples[3] == pytest.approx(0.23136196262203157, rel=0, abs=1e-15)


def test_mulberry32_is_deterministic():
    r1 = mulberry32(42)
    r2 = mulberry32(42)
    for _ in range(20):
        assert r1() == r2()


def test_hash_str_known_values():
    """FNV-1a over ASCII agrees with the JS hashStr.

    Captured out-of-band by running the JS ``hashStr`` on the same
    inputs — these are the exact uint32s datasets.ts would produce.
    """
    assert hash_str("btc/0") == 1815947927
    assert hash_str("spy") == 3577831965
    assert hash_str("") == 2166136261


# ── Series / features ──────────────────────────────────────────────


def test_gen_series_every_kind_emits_180_samples():
    """All nine kinds emit exactly 180 floats (matches TS contract)."""
    for kind in SERIES_KINDS:
        series = gen_series(kind, 42)
        assert len(series) == 180
        assert all(isinstance(x, float) for x in series)


def test_gen_series_unknown_kind_raises():
    with pytest.raises(ValueError, match="unknown series kind"):
        gen_series("does-not-exist", 1)


def test_features_ranges_are_bounded():
    """Feature fields stay in their declared ranges for every dataset."""
    for ds in DATASETS:
        series = gen_series(ds.kind, hash_str(ds.id + "/0"))
        f = features(series)
        assert 0.0 <= f.peak <= 1.0
        assert 0.0 <= f.zc <= 1.0
        assert 0.0 <= f.hurst <= 1.0
        assert f.range >= 0.0
        assert f.sd >= 0.0
        assert not math.isnan(f.slope)


# ── build_points ───────────────────────────────────────────────────


def test_build_points_is_deterministic():
    """Two calls with the same density produce identical outputs."""
    a = build_points(0)
    b = build_points(0)
    assert len(a.points) == len(b.points)
    for p, q in zip(a.points, b.points):
        assert p.id == q.id
        assert p.pos == q.pos
        assert p.feat == q.feat


def test_build_points_density_counts():
    """Density 0/1/2 → 15/32/60 windows per dataset × 9 datasets."""
    assert len(build_points(0).points) == len(DATASETS) * 15
    assert len(build_points(1).points) == len(DATASETS) * 32
    assert len(build_points(2).points) == len(DATASETS) * 60


def test_build_points_rejects_invalid_density():
    with pytest.raises(ValueError):
        build_points(3)  # type: ignore[arg-type]
    with pytest.raises(ValueError):
        build_points(-1)  # type: ignore[arg-type]


def test_build_points_unique_ids():
    pts = build_points(1).points
    ids = [p.id for p in pts]
    assert len(ids) == len(set(ids))


def test_build_points_with_custom_extras():
    """User-added datasets append cleanly and get their own ring slot."""
    extras = (
        Dataset("custom1", "Custom One", "custom", 0xABCDEF, "long-cycle", (2000, 2024)),
    )
    result = build_points(0, extras=extras)
    # Built-in 9 + 1 custom = 10 datasets × 15 windows = 150.
    assert len(result.points) == 10 * 15
    # Custom points appear at the end.
    custom_pts = [p for p in result.points if p.ds == "custom1"]
    assert len(custom_pts) == 15
    # Unknown-domain ring slot is deterministic — two runs agree.
    again = build_points(0, extras=extras).points[-1]
    assert custom_pts[-1].pos == again.pos


# ── Fixture parity (golden file) ──────────────────────────────────


def _load_fixture() -> dict:
    with FIXTURE_PATH.open(encoding="utf-8") as f:
        return json.load(f)


def test_fixture_file_exists_and_has_expected_shape():
    data = _load_fixture()
    assert data["schemaVersion"] == 1
    assert len(data["datasets"]) == len(DATASETS)
    assert len(data["points"]) == len(DATASETS) * 15


def test_build_points_matches_fixture_exactly():
    """Regenerating the fixture in-memory must produce identical bytes.

    This is the canary that fires if anyone changes the generator
    algorithm, feature weights, or layout formulas without updating
    the fixture. If it fires and the change was intentional,
    regenerate via:

        python -m the_similarity.core.spatium_datasets \\
            --density 0 --out the_similarity/tests/fixtures/spatium/points_d0.json
    """
    expected = _load_fixture()
    got = to_export_dict(build_points(0), include_series=False)
    # Compare datasets block first (cheap, catches constants drift).
    assert got["datasets"] == expected["datasets"]
    # Points: length + field-by-field numeric equality. JSON round-trips
    # double precision exactly, so plain == should pass.
    assert len(got["points"]) == len(expected["points"])
    for g, e in zip(got["points"], expected["points"]):
        assert g["id"] == e["id"]
        assert g["ds"] == e["ds"]
        assert g["domain"] == e["domain"]
        assert g["year"] == e["year"]
        assert g["idxInDs"] == e["idxInDs"]
        assert g["pos"] == e["pos"]
        assert g["feat"] == e["feat"]


# ── Distance / similarity / regime ────────────────────────────────


def test_distance_is_zero_for_identity():
    pts = build_points(0).points
    assert distance(pts[0], pts[0]) == 0.0


def test_distance_is_positive_between_different_points():
    pts = build_points(0).points
    assert distance(pts[0], pts[-1]) > 0.0


def test_similarity_maps_zero_to_one_and_large_to_zero():
    assert similarity_from_dist(0.0) == 1.0
    assert similarity_from_dist(10.0) == 0.0


def test_regime_label_buckets():
    from the_similarity.core.spatium_datasets import FeatureVec

    f_trend = FeatureVec(0, 0, 0, 0, 0, 0, 0.7, 0)
    f_rev = FeatureVec(0, 0, 0, 0, 0, 0, 0.3, 0)
    f_rand = FeatureVec(0, 0, 0, 0, 0, 0, 0.5, 0)
    assert regime_label(f_trend) == "Trending"
    assert regime_label(f_rev) == "Mean-rev"
    assert regime_label(f_rand) == "Random"


def test_domain_order_matches_datasets():
    """Every built-in dataset domain is covered by DOMAIN_ORDER."""
    ds_domains = {d.domain for d in DATASETS}
    for d in ds_domains:
        assert d in DOMAIN_ORDER, f"built-in domain {d!r} missing from DOMAIN_ORDER"
